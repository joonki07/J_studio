// 함수이름() { }의 형태를 가지고 있습니다.

// 2. printf();는 콘솔 창에 작성한 내용을 출력하는 기능임니다.
// () 안에는 큰 따옴표("")를 작성하고 큰 따옴표 안에는 작성할 내용을 적습니다.

// 3. return은  함수를 종료하는 키워드입니다. return 다음에 값을 적으면 그 값이
